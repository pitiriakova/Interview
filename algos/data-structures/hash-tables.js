class HashTable {
  // Insert, Delete, Access -  O(1). depends on hash function, Search - O(n)
  // Hash Tables are close to JS Map
  // Linear probing (next empty slot) and separate chaining (store as nested arrays)
  // TODO: now it allows to store duplicate keys and using get() we will get the first found

  constructor(size = 53) {
    this.keyMap = new Array(size)
    // [ [{}, {}], [] ]
  }

  _hashFromObject(key) {
    let total = 0;
    let WEIRD_PRIME = 31;
    for (let i = 0; i < Math.min(key.length, 100); i++) {
      let char = key[i];
      let value = char.charCodeAt(0) - 96
      total = (total * WEIRD_PRIME + value);
    }
      return total;
  }

  _hash(key) {
    // TODO: should be a method of another class instance
    return this._hashFromObject(key) % this.keyMap.length;
  }

  set(key, value) {
    let index = this._hash(key);
    if(!this.keyMap[index]){
      this.keyMap[index] = [];
    }
    this.keyMap[index].push([key, value]);
  }

  get(key) {
    const index = this._hash(key);

    if (this.keyMap[index]) {
      for (let i = 0; i < this.keyMap[index].length; i++) {
        if (this.keyMap[index][i][0] === key) {
          return this.keyMap[index][i][1];
        }
      }
    }

    return undefined;
  }

  /**HASH TABLES 2**/
  keys() {
    let keysArr = [];
    for (let i = 0; i < this.keyMap.length; i++) {
      if (this.keyMap[i]) {
        for (let j = 0; j < this.keyMap[i].length; j++) {
          keysArr.push(this.keyMap[i][j][0]);
        }
      }
    }

    return keysArr;
  }

  values() {
    let valuesArr = [];
    for (let i = 0; i < this.keyMap.length; i++) {
      if (this.keyMap[i]) {
        for (let j = 0; j < this.keyMap[i].length; j++) {
          if(!valuesArr.includes(this.keyMap[i][j][1])) {
            valuesArr.push(this.keyMap[i][j][1]);
          }
        }
      }
    }

    return valuesArr;
  }
}

